function onSpawn(npc)
    npc:initNpcAi()
    npc:setPos(tpz.path.first(path))
    onPath(npc)
end

function onPath(npc)
    tpz.path.patrolsimple(npc, path, flags)
    if npc:atPoint(tpz.path.get(path, 1)) or npc:atPoint(tpz.path.get(path, 25)) then
        npc:setPos(-66.994, -6.000, -133.674, 150)
    elseif npc:atPoint(tpz.path.get(path, 9)) or npc:atPoint(tpz.path.get(path, 39)) then
        npc:setPos(-65.683, -6.000, -130.305, 150)
    elseif npc:atPoint(tpz.path.get(path, 17)) then
