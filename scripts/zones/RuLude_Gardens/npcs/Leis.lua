function onSpawn(npc)
    npc:initNpcAi()
    npc:setPos(tpz.path.first(path))
    onPath(npc)
end

function onPath(npc)
    tpz.path.patrolsimple(npc, path, flags)
    if npc:atPoint(tpz.path.get(path, 1)) or npc:atPoint(tpz.path.get(path, 21)) then
        npc:setPos(-8.826, 1.996, 132.537, 61)
    elseif npc:atPoint(tpz.path.get(path, 11)) or npc:atPoint(tpz.path.get(path, 40)) then
        npc:setPos(-8.294, 1.996, 137.101, 0)
    elseif npc:atPoint(tpz.path.get(path, 30)) or npc:atPoint(tpz.path.get(path, 50)) then
        npc:setPos(-12.483, 1.984, 136.894, 129)
    end
end

